var oso, hospital, tren, noDistraido, derecha, izquierda, alta;

if (oso == noDistraido) {
    console.log("El flaco termina en el hospital");
    if (hospital == alta) {
        console.log("Va a bailar")
    } else {
        console.log("Muere");
    }
} else {
    console.log("Llega hasta la interseccion del tren");
    if (tren == derecha) {
        console.log("El flaco se va a bailar");
    } else if (tren == izquierda) {
        console.log("Termina en un incendio y se muere");
    } else {
        console.log("Se va a dormir la siesta");
    }
}